/** hex色 → {r,g,b} */
function hexToRgb(hex: string): { r: number; g: number; b: number } {
  const h = hex.replace("#", "");
  const n = parseInt(h.length === 3 ? h.split("").map(c => c + c).join("") : h, 16);
  return { r: (n >> 16) & 255, g: (n >> 8) & 255, b: n & 255 };
}

// スプライトシートキャッシュ（バリアント名＋色をキーにする）
const spriteSheetCache = new Map<string, HTMLCanvasElement>();

/** 指定バリアントのスプライトシートをオフスクリーンCanvasに描画して返す（キャッシュ付き） */
function buildSpriteSheet(variant: VariantName, color: string): HTMLCanvasElement {
  const cacheKey = `${variant}__${color}`;
  const cached = spriteSheetCache.get(cacheKey);
  if (cached) return cached;

  const canvas = document.createElement("canvas");
  canvas.width = SHEET_W;
  canvas.height = SHEET_H;
  const ctx = canvas.getContext("2d")!;
  const { r, g, b } = hexToRgb(color);
  const frames = FRAME_VARIANTS[variant];

  for (let i = 0; i < TOTAL_FRAMES; i++) {
    const col = i % COLS;
    const row = Math.floor(i / COLS);
    const ox = col * CELL;
    const oy = row * CELL;
    const frame = frames[i];
    for (const [x, y, a] of frame) {
      ctx.fillStyle = `rgba(${r},${g},${b},${a})`;
      ctx.fillRect(ox + x, oy + y, 1, 1);
    }
  }

  spriteSheetCache.set(cacheKey, canvas);
  return canvas;
}

