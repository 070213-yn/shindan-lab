// === メイン判定関数 ===

/**
 * 正規化スコアからモディファイアを判定する
 *
 * @param normalizedScores - 正規化済みスコア配列（少なくとも9要素）
 *   [0] Passion, [1] Anx, [2] Avo, [3] Pragma, [4] Agape,
 *   [5] Storge, [6] Growth, [7] Assert, [8] Flex
 * @returns モディファイア判定結果
 */
export function determineModifiers(normalizedScores: number[]): ModifierResult {
  // assertスコア（dim 7）: 50超で高主張(α)、50以下で控えめ(β)
  const assertScore = normalizedScores[7] ?? 50;
  // flexスコア（dim 8）: 50超で適応的(+)、50以下で一貫的(-)
  const flexScore = normalizedScores[8] ?? 50;

  const isAlpha = assertScore > 50;
  const isPlus = flexScore > 50;

  const assertType: "α" | "β" = isAlpha ? "α" : "β";
  const flexType: "+" | "-" = isPlus ? "+" : "-";
  const label = `${assertType}${flexType}`;

  const data = MODIFIER_DATA[label];

  return {
    assertType,
    flexType,
    label,
    ...data,
  };
}

/**
 * モディファイアのラベルから直接データを取得する（ユーティリティ）
 *
 * @param label - "α+" | "α-" | "β+" | "β-"
 * @returns モディファイアデータ（ラベルが不正な場合はundefined）
 */
export function getModifierByLabel(
  label: string
): ModifierDataEntry | undefined {
  return MODIFIER_DATA[label];
}

/**
 * 全モディファイアの一覧を取得する（UIでの選択表示用）
 */
export function getAllModifiers(): { label: string; subtitle: string }[] {
  return [
    { label: "α+", subtitle: MODIFIER_DATA["α+"].subtitle },
    { label: "α-", subtitle: MODIFIER_DATA["α-"].subtitle },
    { label: "β+", subtitle: MODIFIER_DATA["β+"].subtitle },
    { label: "β-", subtitle: MODIFIER_DATA["β-"].subtitle },
  ];
}
